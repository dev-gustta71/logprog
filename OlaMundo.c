#include <stdio.h> // entrada no teclado

int main()
{ // inicio

   // variaveis
   // int = inteiro
   // float = real
   // char = caractere
   // char [50] = caractere

   // IGUAL ==
   // DIFERENTE !=
   // %d = inteiro
   // %f = float
   // %s = caractere
   // %c = uma caracter

   int idade = 19;
   float peso = 79;
   char nome[10] = "GUSTAVO";
   char sexo = 'M';

   printf("IDADE: %d \n", idade); // escreval
   printf("PESO: %.2f \n", peso);
   printf("NOME: %s \n", nome);
   printf("SEXO %c \n", sexo);
   return 0;
} // fimalgoritmo
